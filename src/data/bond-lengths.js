/**
 * Returns the bond length for a given element pair and bond order.
 *
 * The lookup is order-independent — `getBondLength('Cl', 'C', 1)` and
 * `getBondLength('C', 'Cl', 1)` return the same value.
 * @param {string} elementA - First element symbol (e.g. `'C'`).
 * @param {string} elementB - Second element symbol (e.g. `'Cl'`).
 * @param {1|2|3|'aromatic'|'dative'} order - Bond order.
 * @returns {number|null} Bond length in Angstroms (Å), or `null` if unknown.
 */
export function getBondLength(elementA, elementB, order) {
  const key = [elementA, elementB].sort().join('-');
  return bondLengths[key]?.[order] ?? null;
}
